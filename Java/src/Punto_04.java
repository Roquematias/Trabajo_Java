public class Punto_04 {
	public static void main(String[] args) {
		int num1 = 66;
		int num2 = 14;

		if (num1 > num2) {
			System.out.println(num1 + " es mayor que " + num2);
		} else if (num1 < num2) {
			System.out.println(num2 + " es mayor que " + num1);
		} else {
			System.out.println("Ambos números son iguales.");
		}
	}
}
